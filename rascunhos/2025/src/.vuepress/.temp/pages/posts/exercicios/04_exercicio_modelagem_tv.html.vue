<p>Utilizando os conhecimentos de O.O.,  e os conceitos de encapsulamento,  construa um programa utilizando as informações abaixo.</p>
<p>Uma televisão possui as seguintes características:</p>
<ul>
<li>tamanho de tela (em polegadas);</li>
<li>volume: de 1 a 10 iniciando em 5 (somente no construtor);</li>
<li>marca;</li>
<li>voltagem (220 e 110);</li>
<li>canal.</li>
</ul>
<p>A televisão pode realizar as seguintes ações:</p>
<ul>
<li>ligar: ao ligar a televisão deve imprimir seu consumo. O consumo deve ser definido pela voltagem multiplicada pela quantidades de polegadas;</li>
<li>desligar;</li>
<li>aumentar e diminuir o volume;</li>
<li>subir e descer canal.</li>
</ul>
</li>
</ol>
